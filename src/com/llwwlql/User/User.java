package com.llwwlql.User;

public class User {
	private String userID;
	private String userName;
	private String nickName;
	private String password;
	private String soloved;
	private String subminted;
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getNickName() {
		return nickName;
	}
	public void setNickName(String nickName) {
		this.nickName = nickName;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getSoloved() {
		return soloved;
	}
	public void setSoloved(String soloved) {
		this.soloved = soloved;
	}
	public String getSubminted() {
		return subminted;
	}
	public void setSubminted(String subminted) {
		this.subminted = subminted;
	}
}
